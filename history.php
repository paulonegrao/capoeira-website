			<p>Abruptly brought to the new American continent, the Africans had to develop forms of protection against
				the
				explotation and repression of colonizers. They were targets of violent practices and punishments of
				<em>senhores de engenho</em>. When they fled from the farms, they were pursued by the captains of the
				bush, who had a very violent running.</p>
			<p>The planters forbade slaves from practicing any kind of struggle. Soon, the slaves used the rhythm and
				movements of their African dances, adapting to a type of camouflaged struggle. Thus started the
				capoeira, a martial art disguised as a dance. It was an unprecedented and important instrument of the
				cultural and physical resistance of the Brazilian slaves.</p>
			<img id="hisimage" src="images/history1.jpg" alt="History of Capoeira">
			<p>The practice of capoeira occurred in <em>terreiros</em> near the slave quarters (sheds that served as
				dormitory
				for slaves) and had as main functions the maintenance of culture, the relief of work stress and the
				maintenance of physical health. Often, the fights took place in fields with small shrubs, called in that
				time as capoeira or capoeirão. From the name of those spaces came the name of the fighting.</p>
			<p>Until the year 1930, the practice of capoeira was prohibited in Brazil, because it was seen as a violent
				and subversive act. The police received instructions to arrest the capoeiristas who practiced this
				fight. In 1930, an important Brazilian capoeirista, master Bimba, presented / displayed the fight for
				the then president Getúlio Vargas. The president liked the art so much that it turned it into a
				Brazilian national sport.</p>
